export function getDailyQuote(dateStr) {
  if (!dateStr) {
    const today = new Date();
    dateStr = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
  }
  let hash = 0;
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash * 31 + dateStr.charCodeAt(i)) % quotes.length;
  }
  return quotes[hash];
}
